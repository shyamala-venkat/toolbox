/**
 * Resolve a tool icon name to its Lucide component. Safe to call with
 * unknown names — returns `FALLBACK_ICON` rather than throwing.
 */
export const getToolIcon = (name: string | undefined | null): LucideIcon => {
  if (!name) return FALLBACK_ICON;
  const key = name.toLowerCase().trim();
  return ICON_REGISTRY[key] ?? FALLBACK_ICON;
};
