/**
 * Tool icon lookup.
 *
 * Each tool declares its icon as a string (Lucide icon name) so the registry
 * can live in a plain data file. Here we map those strings to actual Lucide
 * components at module load time. Unknown names resolve to `FALLBACK_ICON`.
 *
 * Why a static map instead of dynamic `import()`: the Lucide icon set is
 * already tree-shakable and we want zero runtime surprises. Tools are expected
 * to pick from this allowlist; Phase 3 can append entries freely.
 */

